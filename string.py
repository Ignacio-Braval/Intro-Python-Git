myString = "Ignacio"

print(myString.upper())  # convierte el dato guardado en la variable en mayusculas

print(myString.lower())  # convierte el dato guardado en la variable en minusculas

print(myString.replace("hola", "bye"))  # remplaza la palabra o el dato seleccionado por otra

print(myString.count("l"))  # muestra y cuenta, cuantas veces se repite esa letra seleccionada

print(myString.split())  # separa los datos o palabras guardadas en la variable

print("my name is: " + myString)  # concadena los datos, los mostrara juntos

print(f"my name is: {myString}")  # otra forma de concadenar datos
